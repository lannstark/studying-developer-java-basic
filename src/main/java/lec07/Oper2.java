package lec07;

public class Oper2 {

    public static void main(String[] args) {
        int number1 = 7;
        int number2 = 10;

        /**
         * true : 네 말이 맞아 (참)
         * false : 네 말은 틀렸어 흥 (거짓)
         *
         * >= : 왼쪽이 크거나 같다
         * > : 왼쪽이 크다
         * <= : 오른쪽이 크거나 같다
         * < : 오른쪽이 크다
         *
         * == : 같다
         * != : 다르다
         */

        System.out.println(number1 != number2);
    }

}
